"use strict";
/*Question 75: Compound Assignment Operators:
Use compound assignment operators to simplify arithmetic operations.
Start with x = 4 and perform a series of operations (addition, subtraction, multiplication, division)
on x using compound operators.*/
let x = 4;
console.log(`Initial x value is ${x}`);
console.log(`After addition with 2, x is ${x += 2}`);
console.log(`After subtraction with 1, x is ${x -= 1}`);
console.log(`After multiplication with 3, x is ${x *= 3}`);
console.log(`After division with 2, x is ${x /= 2}`);
